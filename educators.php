      // Function to open the popup
function openPopup(popupId) {
  document.getElementById(popupId).style.display = 'block';
}

// Function to close the popup
function closePopup(popupId) {
  document.getElementById(popupId).style.display = 'none';
  resetForm(popupId);
}

// Function to reset form and error messages
function resetForm(popupId) {
  const form = document.querySelector(`#${popupId} form`);
  form.reset();
  const errorMsg = form.querySelector('.error-message');
  if (errorMsg) errorMsg.style.display = 'none';
}

// Function to show error messages
function showError(popupId, message) {
  const errorMsg = document.querySelector(`#${popupId} .error-message`);
  errorMsg.innerText = message;
  errorMsg.style.display = 'block';
  
  // Hide error message after 3 seconds
  setTimeout(() => {
    errorMsg.style.display = 'none';
  }, 3000);
}

// Dummy login submission
function submitLogin() {
  const email = document.getElementById('loginEmail').value;
  const password = document.getElementById('loginPassword').value;

  if (email === "" || password === "") {
    showError('loginPopup', 'Please fill in all fields.');
  } else {
    alert("Login successful!");
    closePopup('loginPopup');
  }
}

// Dummy signup submission
function submitSignup() {
  const email = document.getElementById('signupEmail').value;
  const password = document.getElementById('signupPassword').value;
  const confirmPassword = document.getElementById('confirmPassword').value;

  if (email === "" || password === "" || confirmPassword === "") {
    showError('signupPopup', 'Please fill in all fields.');
  } else if (password !== confirmPassword) {
    showError('signupPopup', 'Passwords do not match.');
  } else {
    alert("Signup successful!");
    closePopup('signupPopup');
  }
}



